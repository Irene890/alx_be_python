# Sript that asks the user for their current age and then calculates how old they will be in 2050
futureyear = 2050
currentyear = 2023

currentage= int(input('How old are you? '))

additionyears = futureyear - currentyear 

futureage = currentage + additionyears

print(f"In 2050, you will be {futureage} years old.")

                         
